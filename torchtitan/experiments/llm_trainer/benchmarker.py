"""
Benchmarker: validates convergence and compares performance between the
current optimized model and a candidate model.

Usage (via shell script):
    NGPU=8 HARDWARE=h100-sm90 ./torchtitan/experiments/llm_trainer/run_benchmarker.sh \
        --module graph_trainer.llama3 \
        --config graph_trainer_llama3_debugmodel

    # Auto-promote if valid AND >= 1% faster:
    NGPU=8 HARDWARE=h100-sm90 ./torchtitan/experiments/llm_trainer/run_benchmarker.sh \
        --promote \
        --module graph_trainer.llama3 \
        --config graph_trainer_llama3_debugmodel

This will:
  1. Load metadata (input shapes, FLOPs info) from optimized_models/
  2. Import both the optimized and candidate GraphModules
  3. Run both for N training steps (default 100) and compare final loss
  4. Benchmark execution time and compute MFU for both
  5. Print a comparison report
  6. With --promote: promote candidate if valid AND >= 1% faster
"""

import argparse
import importlib.util
import json
import os
import re
import sys
from datetime import datetime
from pathlib import Path
from statistics import median

import torch
import torch.distributed as dist
from torch.distributed.device_mesh import DeviceMesh

from torchtitan.distributed import ParallelDims, utils as dist_utils
from torchtitan.tools import utils
from torchtitan.tools.logging import init_logger, logger
from torchtitan.tools.utils import get_peak_flops


def _build_fingerprint(hardware, parallel_dims):
    """Build a directory fingerprint encoding hardware + parallelism.

    Only includes parallelism dimensions > 1 to keep the name concise.
    E.g. "h100-sm90" with tp=2, dp_shard=4 -> "h100-sm90_tp2_fsdp4"
    """
    parts = [hardware]
    dims = [
        ("tp", parallel_dims.tp),
        ("fsdp", parallel_dims.dp_shard),
        ("dp", parallel_dims.dp_replicate),
        ("pp", parallel_dims.pp),
        ("cp", parallel_dims.cp),
        ("ep", parallel_dims.ep),
    ]
    for name, val in dims:
        if val > 1:
            parts.append(f"{name}{val}")
    return "_".join(parts)


_PROMOTE_SPEEDUP_THRESHOLD = 1.01
_PROMOTE_RUNS_DEFAULT = 3


def _parse_args():
    """Parse benchmarker-specific args, forwarding the rest to ConfigManager."""
    parser = argparse.ArgumentParser(add_help=False)
    parser.add_argument("--promote", action="store_true", default=False)
    parser.add_argument(
        "--promote-runs",
        type=int,
        default=_PROMOTE_RUNS_DEFAULT,
        help=(
            "Number of consecutive benchmark runs that must all show "
            f">={(_PROMOTE_SPEEDUP_THRESHOLD - 1) * 100:.0f}%% speedup "
            f"for promotion (default: {_PROMOTE_RUNS_DEFAULT})."
        ),
    )
    parser.add_argument("--num-warmup", type=int, default=5)
    parser.add_argument("--num-bench", type=int, default=20)
    parser.add_argument(
        "--num-model-warmup",
        type=int,
        default=3,
        help=(
            "Number of warmup calls to each model BEFORE the validation "
            "check and benchmarking. Allows models to initialize internal "
            "state (e.g. CUDA graph capture). Default: 3."
        ),
    )
    parser.add_argument(
        "--num-validation-steps",
        type=int,
        default=100,
        help=(
            "Number of training steps to run for validation. The candidate's "
            "final loss must be <= the optimized model's final loss. Default: 100."
        ),
    )
    parser.add_argument(
        "--validation-lr",
        type=float,
        default=1e-3,
        help="Learning rate for SGD updates during validation steps. Default: 1e-3.",
    )
    parser.add_argument(
        "--hardware",
        type=str,
        required=True,
        help="Hardware/software specialization namespace (e.g. h100-sm90).",
    )
    known, remaining = parser.parse_known_args()
    return known, remaining


def _load_metadata(meta_path):
    """Load the metadata JSON generated by the flattener."""
    with open(meta_path) as f:
        return json.load(f)


def _reconstruct_device_mesh(
    mesh_spec: str, parallel_dims: ParallelDims
) -> DeviceMesh | str:
    """Reconstruct a DeviceMesh from its stringified metadata form."""
    mesh_match = re.search(r"DeviceMesh\(\(([^)]*)\)", mesh_spec)
    if mesh_match is None:
        return mesh_spec

    mesh_dims = []
    for dim_name in mesh_match.group(1).split(","):
        dim_name = dim_name.strip()
        if not dim_name:
            continue
        dim_name = dim_name.split("=", 1)[0].strip()
        if dim_name == "loss_mesh":
            dim_name = "loss"
        mesh_dims.append(dim_name)

    if not mesh_dims:
        return mesh_spec

    # Ensure the world mesh exists before resolving any submeshes.
    if parallel_dims._world_mesh is None:
        parallel_dims.build_mesh()

    if mesh_dims == ["world"]:
        return parallel_dims._world_mesh

    try:
        return parallel_dims.get_mesh(mesh_dims)
    except Exception:
        return mesh_spec


def _create_inputs_from_metadata(meta, device, parallel_dims):
    """Create deterministic random tensors matching the metadata specs."""
    torch.manual_seed(42)
    inputs = []
    for spec in meta["input_specs"]:
        if "dtype" in spec:
            dtype = getattr(torch, spec["dtype"].replace("torch.", ""))
            shape = spec["shape"]
            if dtype.is_floating_point:
                inputs.append(torch.randn(shape, dtype=dtype, device=device) * 0.01)
            elif dtype in (torch.int32, torch.int64):
                inputs.append(torch.randint(0, 1000, shape, dtype=dtype, device=device))
            else:
                inputs.append(torch.zeros(shape, dtype=dtype, device=device))
        elif spec.get("type") == "DeviceMesh":
            inputs.append(_reconstruct_device_mesh(spec["value"], parallel_dims))
        else:
            inputs.append(spec["value"])
    return inputs


def _load_graph_module(filepath):
    """Import a model file and return an instantiated GraphModule."""
    if not filepath.exists():
        return None
    spec = importlib.util.spec_from_file_location("_bench_model", str(filepath))
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod.GraphModule()


def _run_validation_steps(model, inputs, num_steps, lr):
    """Run model for num_steps training steps with SGD updates.

    Returns a list of per-step loss values.

    Flattened DTensor graphs currently interleave non-tensor mesh inputs with
    tensor state inputs, and the metadata does not preserve a usable positional
    mapping for "state vs. user data". We therefore infer the mutable state
    tensor inputs from the model outputs themselves: every tensor output after
    the loss is treated as a gradient for the earliest tensor inputs.
    """
    current_inputs = [
        inp.clone() if isinstance(inp, torch.Tensor) else inp
        for inp in inputs
    ]
    state_tensor_positions = None

    losses = []
    for step in range(num_steps):
        with torch.no_grad():
            outputs = model(*current_inputs)

        if isinstance(outputs, torch.Tensor):
            outputs = (outputs,)

        losses.append(outputs[0].item())

        grad_tensors = [out for out in outputs[1:] if isinstance(out, torch.Tensor)]
        if state_tensor_positions is None:
            state_tensor_positions = []
            for input_idx, inp in enumerate(current_inputs):
                if isinstance(inp, torch.Tensor):
                    state_tensor_positions.append(input_idx)
                    if len(state_tensor_positions) == len(grad_tensors):
                        break

            if len(state_tensor_positions) != len(grad_tensors):
                raise RuntimeError(
                    "Unable to align tensor state inputs with gradient outputs: "
                    f"{len(state_tensor_positions)=}, {len(grad_tensors)=}"
                )

        for input_idx, grad in zip(state_tensor_positions, grad_tensors, strict=True):
            state_tensor = current_inputs[input_idx]
            if grad.shape == state_tensor.shape:
                current_inputs[input_idx] = state_tensor - lr * grad

    return losses


def _benchmark_model(model, inputs, num_warmup, num_bench):
    """Benchmark a model's execution time using CUDA events.

    Returns median time in milliseconds.
    """
    for _ in range(num_warmup):
        with torch.no_grad():
            model(*inputs)
    torch.cuda.synchronize()

    times = []
    for _ in range(num_bench):
        start = torch.cuda.Event(enable_timing=True)
        end = torch.cuda.Event(enable_timing=True)
        start.record()
        with torch.no_grad():
            model(*inputs)
        end.record()
        torch.cuda.synchronize()
        times.append(start.elapsed_time(end))

    return median(times)


def _promote_with_mfu_comment(candidate_path, optimized_path, cand_mfu, fingerprint):
    """Copy candidate to optimized, inserting an MFU comment at the top."""
    code = candidate_path.read_text()

    mfu_str = f"{cand_mfu:.2f}%" if cand_mfu is not None else "N/A"
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M")
    comment = f"# MFU: {mfu_str} (promoted {timestamp}, fingerprint: {fingerprint})\n"

    optimized_path.parent.mkdir(parents=True, exist_ok=True)
    optimized_path.write_text(comment + code)


def main():
    init_logger()

    bench_args, remaining_args = _parse_args()

    from torchtitan.config import ConfigManager

    config_manager = ConfigManager()
    sys.argv = [sys.argv[0]] + remaining_args
    config = config_manager.parse_args(remaining_args)

    config.compile.mode = "aot_fx_trace"

    device_type = utils.device_type
    device = torch.device(f"{device_type}:{int(os.environ['LOCAL_RANK'])}")
    torch.cuda.set_device(device)

    torch.use_deterministic_algorithms(True)
    torch.backends.cudnn.deterministic = True
    torch.backends.cudnn.benchmark = False
    os.environ["CUBLAS_WORKSPACE_CONFIG"] = ":4096:8"

    world_size = dist_utils.init_distributed(
        config.comm,
        enable_cpu_backend=config.training.enable_cpu_offload,
        base_folder=config.dump_folder,
    )

    rank = dist.get_rank()
    parallel_dims = ParallelDims.from_config(config.parallelism, world_size)

    model_spec = config.model_spec
    if model_spec is None:
        raise ValueError("model_spec must be set. Pass --module to specify the model.")

    model_name = model_spec.name.split("/")[-1]
    output_name = f"{model_name}_{model_spec.flavor}"
    fingerprint = _build_fingerprint(bench_args.hardware, parallel_dims)

    llm_trainer_dir = Path(__file__).parent
    fingerprint_dir = llm_trainer_dir / "targets" / fingerprint
    optimized_dir = fingerprint_dir / "optimized_models"
    candidate_dir = fingerprint_dir / "candidate_models"

    model_filename = f"{output_name}_rank{rank}.py"
    meta_filename = f"{output_name}_rank{rank}_meta.json"

    meta_path = optimized_dir / meta_filename
    if not meta_path.exists():
        raise FileNotFoundError(
            f"Metadata not found at {meta_path}. "
            f"Run the flattener first to generate the baseline."
        )

    optimized_path = optimized_dir / model_filename
    candidate_path = candidate_dir / model_filename

    if not optimized_path.exists():
        raise FileNotFoundError(
            f"Optimized model not found at {optimized_path}. "
            f"Run the flattener first."
        )
    if not candidate_path.exists():
        raise FileNotFoundError(
            f"Candidate model not found at {candidate_path}. "
            f"Copy the optimized model to {candidate_dir}/ and optimize it."
        )

    meta = _load_metadata(meta_path)

    print(f"\n{'=' * 60}")
    print(f"  LLM Trainer Benchmarker")
    print(f"  Model: {output_name} (rank {rank})")
    print(f"  Fingerprint: {fingerprint}")
    print(f"{'=' * 60}\n")

    print("Loading models...")
    optimized_gm = _load_graph_module(optimized_path)
    candidate_gm = _load_graph_module(candidate_path)
    print(f"  Optimized: {optimized_path}")
    print(f"  Candidate: {candidate_path}")

    print("\nCreating inputs...")
    inputs = _create_inputs_from_metadata(meta, device, parallel_dims)
    num_tensor_inputs = sum(1 for x in inputs if isinstance(x, torch.Tensor))
    print(f"  {len(inputs)} inputs ({num_tensor_inputs} tensors)")

    # Model warmup: let models initialize internal state (e.g. CUDA graph
    # capture, JIT compilation, caching). These calls happen BEFORE the
    # validation check so the check tests the "warmed up" code path.
    if bench_args.num_model_warmup > 0:
        print(f"\nModel warmup: {bench_args.num_model_warmup} calls per model...")
        with torch.no_grad():
            for wi in range(bench_args.num_model_warmup):
                optimized_gm(*inputs)
                candidate_gm(*inputs)
        torch.cuda.synchronize()
        print("  Done.")

    num_steps = bench_args.num_validation_steps
    val_lr = bench_args.validation_lr

    print(f"\n--- Validation ({num_steps} training steps, lr={val_lr}) ---")

    print("  Running optimized model...")
    opt_losses = _run_validation_steps(optimized_gm, inputs, num_steps, val_lr)

    print("  Running candidate model...")
    cand_losses = _run_validation_steps(candidate_gm, inputs, num_steps, val_lr)

    print(f"\n  {'Step':>6}  {'Optimized Loss':>15}  {'Candidate Loss':>15}")
    print(f"  {'----':>6}  {'-' * 15}  {'-' * 15}")
    for step in range(num_steps):
        if step == 0 or (step + 1) % 10 == 0:
            print(
                f"  {step + 1:>6}  {opt_losses[step]:>15.6f}"
                f"  {cand_losses[step]:>15.6f}"
            )

    valid = cand_losses[-1] <= opt_losses[-1]
    if valid:
        print(
            f"\n  RESULT: CANDIDATE VALID "
            f"(final loss {cand_losses[-1]:.6f} <= {opt_losses[-1]:.6f})"
        )
    else:
        print(
            f"\n  RESULT: CANDIDATE FAILED "
            f"(final loss {cand_losses[-1]:.6f} > {opt_losses[-1]:.6f})"
        )

    num_flops_per_token = meta.get("num_flops_per_token", 0)
    seq_len = meta.get("seq_len", 1)
    local_batch_size = meta.get("local_batch_size", 1)
    tokens_per_step = local_batch_size * seq_len
    device_name = torch.cuda.get_device_name(device)
    gpu_peak_flops = get_peak_flops(device_name)

    num_bench_rounds = bench_args.promote_runs if bench_args.promote else 1
    pct = (_PROMOTE_SPEEDUP_THRESHOLD - 1) * 100
    speedups = []
    cand_mfu = None

    for run_idx in range(num_bench_rounds):
        if num_bench_rounds > 1:
            print(
                f"\n--- Performance Benchmark (run {run_idx + 1}/{num_bench_rounds}) ---"
            )
        else:
            print(f"\n--- Performance Benchmark ---")
        print(
            f"  Warmup: {bench_args.num_warmup} iters, "
            f"Bench: {bench_args.num_bench} iters"
        )

        opt_time = _benchmark_model(
            optimized_gm, inputs, bench_args.num_warmup, bench_args.num_bench
        )
        cand_time = _benchmark_model(
            candidate_gm, inputs, bench_args.num_warmup, bench_args.num_bench
        )

        speedup = opt_time / cand_time if cand_time > 0 else float("inf")
        speedups.append(speedup)

        opt_tps = tokens_per_step / (opt_time / 1000.0)
        cand_tps = tokens_per_step / (cand_time / 1000.0)

        if num_flops_per_token > 0 and gpu_peak_flops > 0:
            opt_mfu = 100 * num_flops_per_token * opt_tps / gpu_peak_flops
            cand_mfu = 100 * num_flops_per_token * cand_tps / gpu_peak_flops
        else:
            opt_mfu = None
            cand_mfu = None

        print(f"\n  {'Metric':<25} {'Optimized':>15} {'Candidate':>15}")
        print(f"  {'-' * 55}")
        print(f"  {'Time (ms)':<25} {opt_time:>15.3f} {cand_time:>15.3f}")
        print(f"  {'Tokens/sec':<25} {opt_tps:>15,.0f} {cand_tps:>15,.0f}")
        if opt_mfu is not None and cand_mfu is not None:
            print(f"  {'MFU (%)':<25} {opt_mfu:>15.2f} {cand_mfu:>15.2f}")
        print(f"  {'Speedup':<25} {'':>15} {speedup:>15.3f}x")

        if speedup < _PROMOTE_SPEEDUP_THRESHOLD and bench_args.promote:
            print(
                f"\n  Run {run_idx + 1}: speedup {speedup:.3f}x < {_PROMOTE_SPEEDUP_THRESHOLD:.2f}x threshold, stopping early"
            )
            break

    all_passed = all(s >= _PROMOTE_SPEEDUP_THRESHOLD for s in speedups)
    enough_runs = len(speedups) == num_bench_rounds

    print(f"\n--- Summary ---")
    if not valid:
        print(f"  CANDIDATE FAILED: final loss exceeds baseline")
    elif all_passed and enough_runs:
        print(
            f"  CANDIDATE IS VALID AND >={pct:.0f}% FASTER "
            f"({len(speedups)}/{num_bench_rounds} runs passed)"
        )
    elif any(s >= _PROMOTE_SPEEDUP_THRESHOLD for s in speedups):
        passed = sum(1 for s in speedups if s >= _PROMOTE_SPEEDUP_THRESHOLD)
        print(
            f"  CANDIDATE IS VALID BUT INCONSISTENT "
            f"({passed}/{num_bench_rounds} runs >={pct:.0f}% faster)"
        )
    elif speedups[-1] >= 1.0:
        print(f"  CANDIDATE IS VALID BUT <{pct:.0f}% FASTER (not enough to promote)")
    else:
        print(f"  CANDIDATE IS VALID BUT SLOWER")

    if bench_args.promote:
        can_promote = valid and all_passed and enough_runs
        if can_promote:
            print(f"\n  Promoting candidate to optimized...")
            _promote_with_mfu_comment(
                candidate_path, optimized_path, cand_mfu, fingerprint
            )
            print(f"  Done! {candidate_path.name} -> {optimized_path}")
        elif not valid:
            print(
                f"\n  Cannot promote: candidate final loss exceeds baseline. "
                f"Fix convergence first."
            )
        else:
            print(
                f"\n  Cannot promote: candidate must be >={pct:.0f}% faster "
                f"on all {num_bench_rounds} runs."
            )

    dist.destroy_process_group()


if __name__ == "__main__":
    main()
